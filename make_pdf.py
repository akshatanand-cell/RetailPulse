"""
make_pdf.py — generates RetailPulse_Project_Report.pdf

Pulls real numbers from reports/*.csv (no hardcoded fake metrics) and
embeds the actual charts generated by the notebooks.
"""

import pandas as pd
import os
from reportlab.lib.pagesizes import A4
from reportlab.lib.units import cm
from reportlab.lib import colors
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.enums import TA_CENTER, TA_LEFT
from reportlab.platypus import (
    SimpleDocTemplate, Paragraph, Spacer, Image, Table, TableStyle,
    PageBreak, HRFlowable
)

BASE = os.path.dirname(__file__)
OUT_PDF = os.path.join(BASE, "RetailPulse_Project_Report.pdf")

def load_metric(path, key_col, val_col, key, default="N/A"):
    try:
        df = pd.read_csv(path)
        row = df[df[key_col] == key]
        if len(row):
            val = row[val_col].values[0]
            try:
                return float(val)
            except (ValueError, TypeError):
                return val
    except Exception:
        pass
    return default

forecast_mape = load_metric("reports/forecasting/model_metrics.csv", "metric", "value", "MAPE")
forecast_rmse = load_metric("reports/forecasting/model_metrics.csv", "metric", "value", "RMSE")
forecast_mae  = load_metric("reports/forecasting/model_metrics.csv", "metric", "value", "MAE")
churn_auc     = load_metric("reports/churn/churn_metrics.csv", "metric", "value", "AUC_ROC")
churn_p20     = load_metric("reports/churn/churn_metrics.csv", "metric", "value", "Precision_Top20pct")
churn_rate    = load_metric("reports/churn/churn_metrics.csv", "metric", "value", "Churn_Rate_pct")
high_risk_n   = load_metric("reports/churn/churn_metrics.csv", "metric", "value", "High_Risk_Customers")

try:
    val_df = pd.read_csv("reports/validation/validation_results.csv")
    val_pass = (val_df["status"] == "PASS").sum()
    val_total = len(val_df)
except Exception:
    val_pass, val_total = "N/A", "N/A"

try:
    seg_df = pd.read_csv("data/processed/customer_segments.csv")
    seg_counts = seg_df["segment"].value_counts().to_dict()
    n_segments = seg_df["segment"].nunique()
except Exception:
    seg_counts, n_segments = {}, "N/A"

try:
    drift_df = pd.read_csv("reports/drift/drift_summary.csv")
    drift_stable = (drift_df["status"] == "Stable").sum()
    drift_total = len(drift_df)
except Exception:
    drift_stable, drift_total = "N/A", "N/A"

try:
    eda_summary = pd.read_csv("reports/eda/summary_stats.csv", index_col=0).iloc[:, 0].to_dict()
except Exception:
    eda_summary = {}

styles = getSampleStyleSheet()
title_style = ParagraphStyle("TitleC", parent=styles["Title"], fontSize=26, textColor=colors.HexColor("#1e3a5f"), alignment=TA_CENTER, spaceAfter=6)
sub_style = ParagraphStyle("SubC", parent=styles["Normal"], fontSize=13, textColor=colors.HexColor("#555"), alignment=TA_CENTER, spaceAfter=4)
h1 = ParagraphStyle("H1", parent=styles["Heading1"], fontSize=16, textColor=colors.HexColor("#1e3a5f"), spaceBefore=14, spaceAfter=8)
h2 = ParagraphStyle("H2", parent=styles["Heading2"], fontSize=13, textColor=colors.HexColor("#2563eb"), spaceBefore=10, spaceAfter=6)
body = ParagraphStyle("Body", parent=styles["Normal"], fontSize=10.5, leading=15, spaceAfter=8)
note = ParagraphStyle("Note", parent=styles["Normal"], fontSize=9.5, leading=13, textColor=colors.HexColor("#92400e"),
                       backColor=colors.HexColor("#fef3c7"), borderPadding=8, spaceAfter=10)
caption = ParagraphStyle("Caption", parent=styles["Normal"], fontSize=9, textColor=colors.HexColor("#666"), alignment=TA_CENTER, spaceAfter=10)

story = []

# ---- Cover ----
story.append(Spacer(1, 4*cm))
story.append(Paragraph("RetailPulse", title_style))
story.append(Paragraph("AI-Powered Customer Analytics &amp; Demand Forecasting Platform", sub_style))
